package invadersofspace;

class Tuple{
    private final double x;
    private final double y;
    
    public Tuple(double x, double y){
        this.x = x;
        this.y = y;
    }
    
    public double getX(){
        return this.x;
    }
    
    public double getY(){
        return this.y;
    }
}
